/**
  * @brief GPIO Initialization Function
  * @param None
  * @retval None
  */
void MX_GPIO_Init(void)
{
  GPIO_InitTypeDef GPIO_InitStruct = {0};

  /* GPIO Ports Clock Enable */
  __HAL_RCC_GPIOD_CLK_ENABLE();
  __HAL_RCC_GPIOA_CLK_ENABLE();
  __HAL_RCC_GPIOB_CLK_ENABLE();

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOA, FAN_Pin|LED_Pin|STEP_Pin|ENABLE_Pin 
                          |STEP_DIR_Pin, GPIO_PIN_RESET);

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOB, M2_Pin|M1_Pin|M0_Pin|DATA_DIR_Pin, GPIO_PIN_RESET);

  /*Configure GPIO pins : FAN_Pin LED_Pin STEP_Pin ENABLE_Pin 
                           STEP_DIR_Pin */
  GPIO_InitStruct.Pin = FAN_Pin|LED_Pin|STEP_Pin|ENABLE_Pin 
                          |STEP_DIR_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH;
  HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

  /*Configure GPIO pins : M2_Pin M1_Pin M0_Pin DATA_DIR_Pin */
  GPIO_InitStruct.Pin = M2_Pin|M1_Pin|M0_Pin|DATA_DIR_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH;
  HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

}


/**
  * @brief  This function is executed in case of error occurrence.
  *         Indicates a fault has occured nad blinks at 1 second intervals.
  * @retval None
  */
void Error_Handler(void)
{
  while (1){
    HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, HIGH);
    HAL_Delay(100);
    HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, LOW);
    HAL_Delay(100);  
  }
}